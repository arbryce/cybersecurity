# Assign to a variable a list of ten string elements.
my_list = ["apple", "banana", "cherry", "strawberry", "blueberry", "fig", "grape", "orange", "lemon", "avocado"]

# Print the fourth element
print(my_list[3])

# Print the sixth through tenth elements
print(my_list[5:])

